/**
 * Import glossary terms from an array
 */
export function importGlossaryTerms(
  terms: Array<Omit<GlossaryTerm, 'id' | 'usageCount' | 'createdAt' | 'updatedAt'>>,
  skipDuplicates = true
): { imported: number; skipped: number } {
  const db = getDatabase()
  let imported = 0
  let skipped = 0

  const transaction = db.transaction(() => {
    for (const term of terms) {
      if (skipDuplicates) {
        const existing = findTermBySource(term.sourceTerm, term.projectId || undefined)
        if (existing) {
          skipped++
          continue
        }
      }

      createGlossaryTerm(term)
      imported++
    }
  })

  transaction()
  return { imported, skipped }
}

/**
 * Export glossary terms for a project
 */
export function exportGlossaryTerms(projectId?: string): GlossaryTerm[] {
  return listGlossaryTerms(projectId || null)
}

