/**
 * Get pending suggestions for a project
 */
export function getPendingSuggestions(projectId: string): GlossarySuggestion[] {
  const db = getDatabase()
  const stmt = db.prepare(`
    SELECT id, project_id, chapter_id, source_term, suggested_target, term_type,
           gender, confidence, source_context, status, reviewed_at, created_at
    FROM glossary_suggestions
    WHERE project_id = ? AND status = 'pending'
    ORDER BY confidence DESC, created_at ASC
  `)

  const rows = stmt.all(projectId) as SuggestionRow[]
  return rows.map(rowToSuggestion)
}

/**
 * Accept a suggestion (creates a glossary term)
 */
export function acceptSuggestion(id: string): GlossaryTerm {
  const db = getDatabase()
  const suggestion = getSuggestion(id)

  if (!suggestion) {
    throw new Error(`Suggestion not found: ${id}`)
  }

  const now = new Date().toISOString()

  // Update suggestion status
  db.prepare(`
    UPDATE glossary_suggestions 
    SET status = 'accepted', reviewed_at = ?
    WHERE id = ?
  `).run(now, id)

  // Create the glossary term
  return createGlossaryTerm({
    projectId: suggestion.projectId,
    sourceTerm: suggestion.sourceTerm,
    targetTerm: suggestion.suggestedTarget,
    termType: suggestion.termType,
    gender: suggestion.gender,
    aliases: [],
    autoGenerated: true,
    confidence: suggestion.confidence,
    sourceContext: suggestion.sourceContext,
  })
}

/**
 * Reject a suggestion
 */
export function rejectSuggestion(id: string): void {
  const db = getDatabase()
  const now = new Date().toISOString()

  db.prepare(`
    UPDATE glossary_suggestions 
    SET status = 'rejected', reviewed_at = ?
    WHERE id = ?
  `).run(now, id)
}

/**
 * Merge a suggestion with an existing term
 */
export function mergeSuggestion(id: string, existingTermId: string): void {
  const db = getDatabase()
  const suggestion = getSuggestion(id)
  const existingTerm = getGlossaryTerm(existingTermId)

  if (!suggestion || !existingTerm) {
    throw new Error('Suggestion or existing term not found')
  }

  const now = new Date().toISOString()

  // Add the suggested translation as an alias if different
  if (suggestion.suggestedTarget !== existingTerm.targetTerm) {
    const aliases = [...existingTerm.aliases]
    if (!aliases.includes(suggestion.suggestedTarget)) {
      aliases.push(suggestion.suggestedTarget)
    }
    updateGlossaryTerm(existingTermId, { aliases })
  }

  // Update suggestion status
  db.prepare(`
    UPDATE glossary_suggestions 
    SET status = 'merged', reviewed_at = ?
    WHERE id = ?
  `).run(now, id)
}

/**
 * Get a specific suggestion
 */
export function getSuggestion(id: string): GlossarySuggestion | null {
  const db = getDatabase()
  const stmt = db.prepare(`
    SELECT id, project_id, chapter_id, source_term, suggested_target, term_type,
           gender, confidence, source_context, status, reviewed_at, created_at
    FROM glossary_suggestions
    WHERE id = ?
  `)

  const row = stmt.get(id) as SuggestionRow | undefined
  return row ? rowToSuggestion(row) : null
}

/**
 * Delete old rejected suggestions
 */
export function cleanupRejectedSuggestions(olderThanDays = 30): number {
  const db = getDatabase()
  const cutoff = new Date()
  cutoff.setDate(cutoff.getDate() - olderThanDays)

  const result = db
    .prepare(`
    DELETE FROM glossary_suggestions 
    WHERE status = 'rejected' AND reviewed_at < ?
  `)
    .run(cutoff.toISOString())

  return result.changes
}

