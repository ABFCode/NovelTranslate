interface GlossaryTermRow {
  id: string
  project_id: string | null
  source_term: string
  target_term: string
  term_type: string
  gender: string | null
  pronouns: string | null
  aliases_json: string
  context: string | null
  notes: string | null
  auto_generated: number
  confidence: number
  source_context: string | null
  usage_count: number
  created_at: string
  updated_at: string
}

interface SuggestionRow {
  id: string
  project_id: string
  chapter_id: string | null
  source_term: string
  suggested_target: string
  term_type: string
  gender: string | null
  confidence: number
  source_context: string
  status: string
  reviewed_at: string | null
  created_at: string
}

function rowToGlossaryTerm(row: GlossaryTermRow): GlossaryTerm {
  return {
    id: row.id,
    projectId: row.project_id,
    sourceTerm: row.source_term,
    targetTerm: row.target_term,
    termType: row.term_type as TermType,
    gender: (row.gender as GlossaryGender) || undefined,
    pronouns: row.pronouns || undefined,
    aliases: JSON.parse(row.aliases_json),
    context: row.context || undefined,
    notes: row.notes || undefined,
    autoGenerated: row.auto_generated === 1,
    confidence: row.confidence,
    sourceContext: row.source_context || undefined,
    usageCount: row.usage_count,
    createdAt: row.created_at,
    updatedAt: row.updated_at,
  }
}

function rowToSuggestion(row: SuggestionRow): GlossarySuggestion {
  return {
    id: row.id,
    projectId: row.project_id,
    chapterId: row.chapter_id || undefined,
    sourceTerm: row.source_term,
    suggestedTarget: row.suggested_target,
    termType: row.term_type as TermType,
    gender: (row.gender as GlossaryGender) || undefined,
    confidence: row.confidence,
    sourceContext: row.source_context,
    status: row.status as 'pending' | 'accepted' | 'rejected' | 'merged',
    reviewedAt: row.reviewed_at || undefined,
    createdAt: row.created_at,
  }
}
