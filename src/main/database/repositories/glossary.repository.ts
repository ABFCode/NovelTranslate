/**
 * Create a new glossary term
 */
export function createGlossaryTerm(
  term: Omit<GlossaryTerm, 'id' | 'usageCount' | 'createdAt' | 'updatedAt'>
): GlossaryTerm {
  const db = getDatabase()
  const id = generateId()
  const now = new Date().toISOString()

  const stmt = db.prepare(`
    INSERT INTO glossary_terms (
      id, project_id, source_term, target_term, term_type, gender, pronouns,
      aliases_json, context, notes, auto_generated, confidence, source_context,
      usage_count, created_at, updated_at
    )
    VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, 0, ?, ?)
  `)

  stmt.run(
    id,
    term.projectId,
    term.sourceTerm,
    term.targetTerm,
    term.termType,
    term.gender || null,
    term.pronouns || null,
    JSON.stringify(term.aliases || []),
    term.context || null,
    term.notes || null,
    term.autoGenerated ? 1 : 0,
    term.confidence,
    term.sourceContext || null,
    now,
    now
  )

  return {
    id,
    ...term,
    aliases: term.aliases || [],
    usageCount: 0,
    createdAt: now,
    updatedAt: now,
  }
}

/**
 * Get a glossary term by ID
 */
export function getGlossaryTerm(id: string): GlossaryTerm | null {
  const db = getDatabase()
  const stmt = db.prepare(`
    SELECT id, project_id, source_term, target_term, term_type, gender, pronouns,
           aliases_json, context, notes, auto_generated, confidence, source_context,
           usage_count, created_at, updated_at
    FROM glossary_terms
    WHERE id = ?
  `)

  const row = stmt.get(id) as GlossaryTermRow | undefined
  return row ? rowToGlossaryTerm(row) : null
}

/**
 * Get all glossary terms for a project (including global terms)
 */
export function listGlossaryTerms(projectId: string | null): GlossaryTerm[] {
  const db = getDatabase()

  let stmt
  if (projectId) {
    // Get project-specific terms and global terms
    stmt = db.prepare(`
      SELECT id, project_id, source_term, target_term, term_type, gender, pronouns,
             aliases_json, context, notes, auto_generated, confidence, source_context,
             usage_count, created_at, updated_at
      FROM glossary_terms
      WHERE project_id = ? OR project_id IS NULL
      ORDER BY source_term ASC
    `)
    return (stmt.all(projectId) as GlossaryTermRow[]).map(rowToGlossaryTerm)
  } else {
    // Get only global terms
    stmt = db.prepare(`
      SELECT id, project_id, source_term, target_term, term_type, gender, pronouns,
             aliases_json, context, notes, auto_generated, confidence, source_context,
             usage_count, created_at, updated_at
      FROM glossary_terms
      WHERE project_id IS NULL
      ORDER BY source_term ASC
    `)
    return (stmt.all() as GlossaryTermRow[]).map(rowToGlossaryTerm)
  }
}

/**
 * Search glossary terms
 */
export function searchGlossaryTerms(
  query: string,
  projectId?: string,
  termType?: TermType
): GlossaryTerm[] {
  const db = getDatabase()
  const searchPattern = `%${query}%`

  let sql = `
    SELECT id, project_id, source_term, target_term, term_type, gender, pronouns,
           aliases_json, context, notes, auto_generated, confidence, source_context,
           usage_count, created_at, updated_at
    FROM glossary_terms
    WHERE (source_term LIKE ? OR target_term LIKE ? OR aliases_json LIKE ?)
  `
  const params: unknown[] = [searchPattern, searchPattern, searchPattern]

  if (projectId) {
    sql += ' AND (project_id = ? OR project_id IS NULL)'
    params.push(projectId)
  }

  if (termType) {
    sql += ' AND term_type = ?'
    params.push(termType)
  }

  sql += ' ORDER BY usage_count DESC, source_term ASC'

  const rows = db.prepare(sql).all(...params) as GlossaryTermRow[]
  return rows.map(rowToGlossaryTerm)
}

/**
 * Update a glossary term
 */
export function updateGlossaryTerm(
  id: string,
  updates: Partial<Omit<GlossaryTerm, 'id' | 'usageCount' | 'createdAt' | 'updatedAt'>>
): void {
  const db = getDatabase()
  const now = new Date().toISOString()

  const setClauses: string[] = ['updated_at = ?']
  const values: unknown[] = [now]

  if (updates.projectId !== undefined) {
    setClauses.push('project_id = ?')
    values.push(updates.projectId)
  }
  if (updates.sourceTerm !== undefined) {
    setClauses.push('source_term = ?')
    values.push(updates.sourceTerm)
  }
  if (updates.targetTerm !== undefined) {
    setClauses.push('target_term = ?')
    values.push(updates.targetTerm)
  }
  if (updates.termType !== undefined) {
    setClauses.push('term_type = ?')
    values.push(updates.termType)
  }
  if (updates.gender !== undefined) {
    setClauses.push('gender = ?')
    values.push(updates.gender || null)
  }
  if (updates.pronouns !== undefined) {
    setClauses.push('pronouns = ?')
    values.push(updates.pronouns || null)
  }
  if (updates.aliases !== undefined) {
    setClauses.push('aliases_json = ?')
    values.push(JSON.stringify(updates.aliases))
  }
  if (updates.context !== undefined) {
    setClauses.push('context = ?')
    values.push(updates.context || null)
  }
  if (updates.notes !== undefined) {
    setClauses.push('notes = ?')
    values.push(updates.notes || null)
  }
  if (updates.confidence !== undefined) {
    setClauses.push('confidence = ?')
    values.push(updates.confidence)
  }

  values.push(id)
  const stmt = db.prepare(`UPDATE glossary_terms SET ${setClauses.join(', ')} WHERE id = ?`)
  stmt.run(...values)
}

/**
 * Delete a glossary term
 */
export function deleteGlossaryTerm(id: string): void {
  const db = getDatabase()
  db.prepare('DELETE FROM glossary_terms WHERE id = ?').run(id)
}

/**
 * Increment usage count for terms
 */
export function incrementTermUsage(termIds: string[]): void {
  if (termIds.length === 0) return
  const db = getDatabase()
  const placeholders = termIds.map(() => '?').join(', ')
  db.prepare(
    `UPDATE glossary_terms SET usage_count = usage_count + 1 WHERE id IN (${placeholders})`
  ).run(...termIds)
}

/**
 * Find a term by source text (exact match)
 */
export function findTermBySource(sourceTerm: string, projectId?: string): GlossaryTerm | null {
  const db = getDatabase()

  let stmt
  if (projectId) {
    stmt = db.prepare(`
      SELECT id, project_id, source_term, target_term, term_type, gender, pronouns,
             aliases_json, context, notes, auto_generated, confidence, source_context,
             usage_count, created_at, updated_at
      FROM glossary_terms
      WHERE source_term = ? AND (project_id = ? OR project_id IS NULL)
      ORDER BY project_id DESC
      LIMIT 1
    `)
    const row = stmt.get(sourceTerm, projectId) as GlossaryTermRow | undefined
    return row ? rowToGlossaryTerm(row) : null
  } else {
    stmt = db.prepare(`
      SELECT id, project_id, source_term, target_term, term_type, gender, pronouns,
             aliases_json, context, notes, auto_generated, confidence, source_context,
             usage_count, created_at, updated_at
      FROM glossary_terms
      WHERE source_term = ? AND project_id IS NULL
      LIMIT 1
    `)
    const row = stmt.get(sourceTerm) as GlossaryTermRow | undefined
    return row ? rowToGlossaryTerm(row) : null
  }
}

